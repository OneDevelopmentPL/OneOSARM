void gfx_draw_char(int x, int y, char c, unsigned int fg_color, unsigned int bg_color)
{
    unsigned char *glyph = (unsigned char *)font8x8[(unsigned char)c];
    asm volatile("dmb sy" ::: "memory");
    
    for (int row = 0; row < 8; row++) {
        unsigned char byte = glyph[row];
        for (int col = 0; col < 8; col++) {
            unsigned int color = (byte & (1 << col)) ? fg_color : bg_color;
            /* Draw 2x2 block for each pixel (scale up from 8x8 -> 16x16) */
            fb_draw_pixel(x + col * 2,     y + row * 2,     color);
            fb_draw_pixel(x + col * 2 + 1, y + row * 2,     color);
            fb_draw_pixel(x + col * 2,     y + row * 2 + 1, color);
            fb_draw_pixel(x + col * 2 + 1, y + row * 2 + 1, color);
        }
    }
}

void gfx_draw_string(int x, int y, const char *str, unsigned int fg_color, unsigned int bg_color)
{
    int pos_x = x;
    
    while (*str) {
        if (*str == '\n') {
            pos_x = x;
            y += 16;
        } else {
            gfx_draw_char(pos_x, y, *str, fg_color, bg_color);
            pos_x += 16;
        }
        str++;
    }
}

void gfx_draw_window(int x, int y, int width, int height, unsigned int border_color, unsigned int bg_color)
{
    /* Draw border */
    fb_draw_hline(x, y, width, border_color);
    fb_draw_hline(x, y + height - 1, width, border_color);
    fb_draw_vline(x, y, height, border_color);
    fb_draw_vline(x + width - 1, y, height, border_color);
    
    /* Fill background */
    fb_draw_rect(x + 1, y + 1, width - 2, height - 2, bg_color);
}

void gfx_draw_button(int x, int y, int width, int height, const char *label, unsigned int color)
{
    gfx_draw_window(x, y, width, height, COLOR_DARK_BLUE, color);
    
    /* Draw label */
    int label_x = x + (width - (8 * 8)) / 2;
    int label_y = y + (height - 8) / 2;
    gfx_draw_string(label_x, label_y, label, COLOR_BLACK, color);
}
